package housingManagment.hms.enums;

/**
 * Enum representing different types of maintenance requests
 */
public enum MaintenanceRequestType {
    PLUMBING, // Issues with water systems, pipes, drains, toilets, etc.
    ELECTRICAL, // Issues with electrical systems, lighting, outlets, etc.
    HEATING, // Issues with heating systems, radiators, thermostats, etc.
    APPLIANCE, // Issues with appliances like refrigerators, stoves, ovens, etc.
    FURNITURE, // Issues with furniture items like beds, desks, chairs, etc.
    STRUCTURAL, // Issues with the building structure like walls, ceilings, doors, etc.
    OTHER // Any other type of maintenance request
}